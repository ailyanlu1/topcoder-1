// O(N) time
class DNASequence {
  public:
  int longestDNASequence(string S) {
    int res=0,l=0;
    FORR(c,S) {
      if(c=='A'||c=='C'||c=='G'||c=='T') ++l;
      else l=0;
      res=max(res,l);
    }
    return res;
  }
};


class MagicalGirlLevelOneDivTwo {
  public:
  double theMinDistance(int D, int X, int Y) {
    double res=hypot(1000,1000);
    for(int x=-1000; x<=1000; ++x) {
      for(int y=-1000; y<=1000; ++y) {
        if(abs(x-X)<=D&&abs(y-Y)<=D) res=min(res,hypot(x,y));
      }
    }
    return res;
  }
};


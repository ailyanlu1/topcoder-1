class SolitaireSimulation {
  public:
  map<VI,int> viz;
  
  VI f(VI A) {
    VI B;
    int N=SZ(A);
    REP(i,N) {
      A[i]--;
      if(A[i]>0) B.push_back(A[i]);
    }
    B.push_back(N);
    sort(B.begin(),B.end());
    return B;
  }
  int periodLength(vector<int> A) {
    sort(A.begin(),A.end());
    int t=0;
    viz[A]=t;
    
    while(true) {
      VI B=f(A);
      ++t;
      if(viz.count(B)) return t-viz[B];
      viz[B]=t;
      A=B;
    }
    return 0;
  }
};


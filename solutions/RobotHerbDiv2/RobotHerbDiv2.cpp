vector< pair < int, int > >  moves = { {0,1},{1,0},{0,-1},{-1,0} };
class RobotHerbDiv2 {
  public:
  int getdist(int T, vector<int> D) {
    int mi=0,x=0,y=0;
    REP(_,T) {
      FORR(d,D) {
        int dx=moves[mi].first,dy=moves[mi].second;
        x+=dx*d,y+=dy*d;
        mi+=d,mi%=4;
      }
    }
    return abs(x)+abs(y);
  }
};


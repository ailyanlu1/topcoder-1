class CardboardBoxes {
  public:
  LL suf(LL w, LL l, LL h) {
    return 2LL*w*h+2LL*l*h+2LL*(w+l)*min(w,l);
  }
  LL f(LL p, LL q) {
    vector<LL> xs={p-1,q-1,p/2};
    return *min_element(ALL(xs));
    /*
    if(p<=q) return p/2;
    return min(q-1,p/2);*/
  }
  long long count(long long S) {
    if(S%2==1) return 0;
    S/=2;
    LL res=0;
    for(LL p=2; p*p<=S; ++p) if(S%p==0) {
      LL q=S/p;
      if(p==q) res+=f(p,q);
      else res+=f(p,q)+f(q,p);
    }
    return res;
  }
};


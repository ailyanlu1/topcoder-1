set<char> C1={'A','B','C','D','E','F'};
map<char,char> C2={{'T','7'},{'S','5'},{'Z','2'},{'I','1'},{'O','0'}};
set<string> C3={"L","LL","U","UL","ULL","LU","LLU"};
class EllysCodeConstants {
  public:
  string getLiteral(string S) {
    int N=SZ(S);
    string res="0x",a,b;
    REP(i,N) {
      char c=S[i];
      if(C1.count(c)) a.push_back(c);
      else if(C2.count(c)) a.push_back(C2[c]);
      else {
        string t=S.substr(i);
        if(C3.count(t)) b=t;
        else a="";
        break;
      }
    }
    if(a.empty()) return "";
    return res+a+b;
  }
};


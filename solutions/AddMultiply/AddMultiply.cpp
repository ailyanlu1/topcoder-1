class AddMultiply {
  public:
  vector<int> makeExpression(int y) {
    for(int x0=-1000; x0<=1000; ++x0) for(int x1=-1000; x1<=1000; ++x1) {
      if(x0==0||x0==1) continue;
      if(x1==0||x1==1) continue;
      
      int x2=y-x0*x1;
      if(-1000<=x2&&x2<=1000&&x2!=0&&x2!=1) return {x0,x1,x2};
    }
    assert(false);
    return {0,0,0};
  }
};


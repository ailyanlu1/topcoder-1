class ColoringRectangle {
  public:
  const double eps=1e-5;
  const int Inf=1e4;
  int W,H;
  int f(VI &R1, VI &R2) {
    VI R[2]={R1,R2};
    int ii[2]={0,0};
    int s=0,res=0;
    double w=0;
    while(true) {
      int i=ii[s]++;
      if(i>=SZ(R[s])) return Inf;
      ++res;
      double r=R[s][i]/2.0;
      double d=sqrt(4*r*r-H*H);
      w+=d;
//      dump4(i,r,d,w);
      if(w+eps>=W) {
//        dump2(w,res);
        return res;
      }
      s^=1;
    }
    return Inf;
  }
  int chooseDisks(int W, int H, vector<int> R, vector<int> B) {
    this->W=W,this->H=H+eps;
    sort(R.rbegin(),R.rend()),sort(B.rbegin(),B.rend());
    int res=min(f(R,B),f(B,R));
    return res==Inf?-1:res;
  }
};


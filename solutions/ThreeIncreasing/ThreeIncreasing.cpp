class ThreeIncreasing {
public:
  int minEaten(int a, int b, int c) {
    if (a<=0||b<=1||c<=2) return -1;
    int res=0;
    int b2 = max(0,b-(c-1));
    b = b-b2; res+= b2;
    int a2 = max(0,a-(b-1));
    res+=a2;
    
    return res;
  }
};

class ThreeIncreasing_org {
  public:
  int minEaten(int a, int b, int c) {
    int b2=min(c-1,b);
    int res=b-b2;
    if (b-b2<0||b2<1) return -1;
    b=b2;
    
    int a2=min(b-1,a);
    res+=a-a2;
    if (a-a2<0||a2<1) return -1;
    
    return res;
  }
};


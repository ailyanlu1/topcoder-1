class TrafficCongestionDivTwo {
  public:
  LL f(int d, int h) {
    assert(h%2==1);
    if(d>h) return 0;
    return (1LL<<d)+f(d+2,h);
  }
  long long theMinCars(int H) {
    if(H==0) return 1;
    return H%2==0?1LL+2LL*f(0,H-1):f(0,H);
  }
};


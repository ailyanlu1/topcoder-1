class TheProgrammingContestDivTwo {
  public:
  vector<int> find(int T, vector<int> X) {
    sort(X.begin(),X.end());
    int N=SZ(X);
    int p=0,sum=0,i=0;
    for(; i<N; ++i) {
      if(sum+X[i]>T) break;
      sum+=X[i];
      p+=sum;
    }
    return {i,p};
  }
};


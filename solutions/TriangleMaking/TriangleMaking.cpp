class TriangleMaking {
public:
  int maxPerimeter(int a, int b, int c) {
    int res=0;
    for(int i=1;i<=a;++i) {
      for(int j=1;j<=b;++j) {
        for(int k=1;k<=c;++k) {
          if(2*max(i,max(j,k))<i+j+k) res=max(res,i+j+k);
        }
      }
    }
    
    return res;
  }
};

class TriangleMaking_org {
  public:
  int maxPerimeter(int a, int b, int c) {
    if (a<b) swap(a,b);
    if (a<c) swap(a,c);
    
    a = min(a, b+c-1);
    
    return a+b+c;
  }
};


class ProblemSetsEasy {
  public:
  bool t(int E, int EM, int M, int MH, int H, int T) {
    if(T>E) EM-=T-E;
    if(T>H) MH-=T-H;
    return EM>=0&&MH>=0&&EM+MH+M>=T;
  }
  int maxSets(int E, int EM, int M, int MH, int H) {
    int ok=min({E,M,H}),ng=3*1e5+1;
    while(abs(ok-ng)>1) {
      int mid=(ok+ng)/2;
      if(t(E,EM,M,MH,H,mid)) ok=mid;
      else ng=mid;
    }
    return ok;
  }
};


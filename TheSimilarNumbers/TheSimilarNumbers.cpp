/*
 
 12/18/2017
 
 11:57-12:14 proof
 12:15 submit and system test passed (183.69 pt)
 
 We want to find as many not-similar numbers as possible.
 That means x>10*y or y>10*x needs to be satisfied.
 
 Since x<y, it's impossible to satisfy x>10*y.
 We'll find y>10*x => y>=10*x+1
 
 In order to get max number of set S which holds S[i+1]>=10*S[i]+1,
 We can have S[0] = L.
 
 Editorial:
  - https://apps.topcoder.com/wiki/display/tc/SRM+568
  - http://kmjp.hatenablog.jp/entry/2013/01/30/0930
  - http://torus711.hatenablog.com/entry/20130130/p2

 */
class TheSimilarNumbers {
  public:
  int find(int L, int B) {
    int n=L,res=0;
    while(n<=B) {
      ++res;
      n=10*n+1;
    }
    return res;
  }
};


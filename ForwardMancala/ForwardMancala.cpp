class ForwardMancala {
public:
  vector<int> findMoves(vector<int> ns) {
    int N=SZ(ns);
    int sum=accumulate(ns.begin(),ns.end(),0);
    
    VI res;
    while(true) {
      if (ns[0]==sum) break;
      int i=1;
      for(;i<N&&!ns[i];++i) {}
      res.push_back(i);
      int v=ns[i]; ns[i]=0;
      for(i=(i+1)%N; v>0; --v, i=(i+1)%N) {
        ++ns[i];
      }
    }
    
    return res;
  }
};

class ForwardMancala_org {
  public:
  vector<int> findMoves(vector<int> ns) {
    int N=SZ(ns);
    
    VI res;
    while(true) {
      int i=1;
      while(i<N) {
        if (ns[i]!=0) break;
        ++i;
      }
      if (i==N) break;
      
      res.push_back(i);
      
      int v=ns[i]; ns[i]=0;
      i = (i+1)%N;
      while(v>0) {
        ++ns[i]; --v;
        i=(i+1)%N;
      }
    }
    
    return res;
  }
};


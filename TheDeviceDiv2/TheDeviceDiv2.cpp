class TheDeviceDiv2 {
  public:
  string yes="YES",no="NO";
  string identify(vector<string> P) {
    int N=SZ(P),M=SZ(P[0]);
    REP(j,M) {
      int c0=0,c1=0;
      REP(i,N) (P[i][j]=='1'?c1:c0)++;
      if(c0>=1&&c1>=2) continue;
      return no;
    }
    return yes;
  }
};

